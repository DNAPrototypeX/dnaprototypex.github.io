    <td colspan="3"><p align="center" class="coltitre">Revue  publi&eacute;e par l&rsquo;Universit&eacute; de Glasgow</p>
      <p>Le <em>Nouveau Moli&eacute;riste</em> incorpore dans son  titre le nom d&rsquo;une revue c&eacute;l&egrave;bre en son temps, et que citent encore les amis de  Moli&egrave;re, <em>Le Moli&eacute;riste</em>. M. Georges  Monval, biblioth&eacute;caire &agrave; la Com&eacute;die-Fran&ccedil;aise, fut le fondateur et r&eacute;dacteur en  chef de cette derni&egrave;re revue, dont il assura &agrave; lui seul la publication une  d&eacute;cennie durant (1879-1889). Ce fut surtout l&rsquo;&eacute;poque des biographes, dont  l&rsquo;int&eacute;r&ecirc;t se porta sur la d&eacute;couverte des p&eacute;riples de Moli&egrave;re dans les  provinces, les sources de ses com&eacute;dies et ses autographes. Depuis la  disparition du <em>Moli&eacute;riste</em>, il n&rsquo;y a  plus de revue consacr&eacute;e exclusivement &agrave; l&rsquo;&oelig;uvre de celui qui, au dire de  Boileau et de bien d&rsquo;autres encore, &eacute;tait le plus grand des auteurs de son  &eacute;poque. Vu l&rsquo;int&eacute;r&ecirc;t croissant que l&rsquo;on porte au th&eacute;&acirc;tre de Moli&egrave;re nous avons  jug&eacute; utile de relancer cette revue, sous le titre du <em>Nouveau Moli&eacute;riste</em>. <br />
        La mission  que se fixe <em>Le Nouveau Moli&eacute;riste</em> est  d&rsquo;une envergure internationale. Il se veut avant tout un organe au service de  toute la communaut&eacute; des Moli&eacute;ristes, leur servant de lieu de d&eacute;bat, de  discussion, portant sur tous les aspects du th&eacute;&acirc;tre de Moli&egrave;re, son  interpr&eacute;tation, sa mise en sc&egrave;ne, sa diffusion, qu&rsquo;il s&rsquo;agisse de synth&egrave;ses m&ucirc;ries  ou travaux originaux, bref, toute contribution qui constitue un r&eacute;el apport &agrave;  la somme de nos connaissances de sa vie et de son &oelig;uvre. Nous tenons aussi &agrave;  publier des comptes rendus des ouvrages relatifs &agrave; son th&eacute;&acirc;tre, ainsi que des  analyses des repr&eacute;sentations contemporaines notables, ou qui ont fait date dans  l&rsquo;histoire du th&eacute;&acirc;tre. Le premier num&eacute;ro, avec un avant-propos du Ministre de  la Culture et de la Francophonie, fut pr&eacute;sent&eacute; &agrave; une r&eacute;ception &agrave; la Sorbonne,  le 28 octobre 1994.</p></td>
